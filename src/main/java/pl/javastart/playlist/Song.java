package pl.javastart.playlist;

/**
 *
 * @author ET4
 */
public class Song {
    private String title;
    private int time;

    public Song(String title, int time) {
        this.title = title;
        this.time = time;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public int getTime() {
        return time;
    }

    public void setTime(int time) {
        this.time = time;
    }
    
    
}
